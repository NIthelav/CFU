#include <iostream>

using std::cout;
using std::endl;

int main()
{
	int a1 = 3, a2 = 3.14;
	cout << "int 3 = " << a1 << endl;
	cout << "int 3.14 = " << a2 << endl << endl;

	double f1 = 3, f2 = 3.14;
	cout << "double 3 = " << f1 << endl;
	cout << "double 3.14 = " << f2 << endl << endl;
}